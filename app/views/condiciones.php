
	<h2>Instrucciones</h2>
	<p>Una vez se abra el plazo de petici&oacute;n de taquillas podrás
	navegar por los edificios para encontrar la taquilla que mejor se adapte
	a tus necesidades.</p>
	<h2>Condiciones generales</h2>
	<ol>
		<li>El periodo de alquiler comprende todo el curso académico y permite
		su uso desde el día de su pago hasta el 15 de Septiembre del año
		siguiente.</li>
		<li>Las taquillas deberán quedar libres de candado después de esa
		fecha, a partir de la cual Delegación de Estudiantes procederá a la
		apertura forzosa de las que aún continuaran cerradas, para permitir la
		entrada de los nuevos adjudicatarios. Aquellas personas que no
		desalojen en los plazos establecidos serán penalizados.<br />
		Todos los enseres retirados de las taquillas serán almacenados durante
		30 días naturales, siendo destruidos a partir de esa fecha si no son
		reclamados por el anterior usuario de taquilla.</li>
		<li>Los adjudicatarios se comprometen al cuidado permanente de la
		taquilla que les ha correspondido, y a no depositar en la misma
		elementos o sustancias peligrosas o molestas, ni que puedan dañar los
		muebles, ni a los usuarios de las demás taquillas.</li>
		<li>Cualquier desperfecto no causado por el usuario deberá ser
		reportado a la Delegación de Estudiantes, a fin de que sea subsanado lo
		antes posible.</li>
		<li>Delegación de Estudiantes y la
		Universidad Carlos III de Madrid no se responsabilizan de los robos o
		sustracciones que pudieran darse en las taquillas.</li>
	</ol>

